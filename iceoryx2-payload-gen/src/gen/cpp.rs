use super::cpp_type;
use crate::ast::{FieldType, Message};

pub fn generate(msg: &Message, service_name: &str) -> String {
    let mut out = String::new();

    out.push_str("// Auto-generated by iox2gen. Do not edit.\n\n");
    out.push_str("#pragma once\n\n");
    out.push_str("#include <cstddef>\n");
    out.push_str("#include <cstdint>\n\n");

    // Emit StaticString<N> template only when needed.
    // Memory layout mirrors Rust's iceoryx2_bb_container::string::StaticString<N>:
    //   char data[N], char terminator, (padding), uint64_t len
    if msg
        .fields
        .iter()
        .any(|f| matches!(f.ty, FieldType::BoundedString(_)))
    {
        out.push_str("template<std::size_t N>\n");
        out.push_str("struct StaticString {\n");
        out.push_str("    char data[N]{};\n");
        out.push_str("    char terminator{};\n");
        out.push_str("    uint64_t len{};\n");
        out.push_str("};\n\n");
    }

    for c in &msg.constants {
        out.push_str(&format!(
            "constexpr {} {} {{{}}};\n",
            cpp_type(c.ty),
            c.name,
            c.value
        ));
    }
    if !msg.constants.is_empty() {
        out.push('\n');
    }

    out.push_str(&format!("struct {} {{\n", msg.name));
    out.push_str(&format!(
        "    static constexpr const char* IOX2_TYPE_NAME = \"{}\";\n",
        msg.name
    ));

    for field in &msg.fields {
        match &field.ty {
            FieldType::Primitive(p) => {
                out.push_str(&format!("    {} {}{{}};\n", cpp_type(*p), field.name));
            }
            FieldType::FixedArray(p, n) => {
                out.push_str(&format!(
                    "    {} {}[{}]{{}};\n",
                    cpp_type(*p),
                    field.name,
                    n
                ));
            }
            FieldType::BoundedString(n) => {
                out.push_str(&format!("    StaticString<{}> {}{{}};\n", n, field.name));
            }
        }
    }

    out.push_str("};\n\n");
    out.push_str(&format!("struct {}Service {{\n", msg.name));
    out.push_str(&format!(
        "    static constexpr const char* SERVICE_NAME = \"{}\";\n",
        service_name
    ));
    out.push_str(&format!(
        "    static constexpr const char* PAYLOAD_TYPE_NAME = {}::IOX2_TYPE_NAME;\n",
        msg.name
    ));
    out.push_str("};\n");
    out
}

#[cfg(test)]
mod tests {
    use super::generate;
    use crate::ast::Message;

    #[test]
    fn generates_cpp_service_structure() {
        let msg = Message {
            name: "Pose".to_string(),
            fields: vec![],
            constants: vec![],
        };

        let generated = generate(&msg, "ros/Pose");

        assert!(generated.contains("static constexpr const char* IOX2_TYPE_NAME = \"Pose\";"));
        assert!(generated.contains("struct PoseService"));
        assert!(generated.contains("static constexpr const char* SERVICE_NAME = \"ros/Pose\";"));
        assert!(generated.contains("PAYLOAD_TYPE_NAME = Pose::IOX2_TYPE_NAME;"));
    }
}
