use super::rust_type;
use crate::ast::{FieldType, Message};

pub fn generate(msg: &Message, service_name: &str) -> String {
    let mut out = String::new();
    let snake_name = to_snake_identifier(&msg.name);
    let upper_name = snake_name.to_ascii_uppercase();

    out.push_str("// Auto-generated by iox2gen. Do not edit.\n\n");
    out.push_str("use iceoryx2::prelude::*;\n");
    out.push_str("use iceoryx2::service::port_factory::publish_subscribe;\n");
    if msg
        .fields
        .iter()
        .any(|f| matches!(f.ty, FieldType::BoundedString(_)))
    {
        out.push_str("use iceoryx2_bb_container::string::StaticString;\n");
    }
    out.push('\n');

    for c in &msg.constants {
        out.push_str(&format!(
            "pub const {}: {} = {};\n",
            c.name,
            rust_type(c.ty),
            c.value
        ));
    }
    if !msg.constants.is_empty() {
        out.push('\n');
    }

    out.push_str("#[repr(C)]\n");
    out.push_str("#[derive(Debug, Clone, Copy, PartialEq, ZeroCopySend)]\n");
    out.push_str(&format!("#[type_name(\"{}\")]\n", msg.name));
    out.push_str(&format!("pub struct {} {{\n", msg.name));

    for field in &msg.fields {
        let type_str = match &field.ty {
            FieldType::Primitive(p) => rust_type(*p).to_string(),
            FieldType::FixedArray(p, n) => format!("[{}; {}]", rust_type(*p), n),
            FieldType::BoundedString(n) => format!("StaticString<{}>", n),
        };
        out.push_str(&format!("    pub {}: {},\n", field.name, type_str));
    }

    out.push_str("}\n\n");

    out.push_str(&format!("impl Default for {} {{\n", msg.name));
    out.push_str("    fn default() -> Self {\n");
    out.push_str("        Self {\n");
    for field in &msg.fields {
        let default_val = match &field.ty {
            FieldType::Primitive(_) => "Default::default()".to_string(),
            FieldType::FixedArray(_, n) => format!("[Default::default(); {}]", n),
            FieldType::BoundedString(_) => "Default::default()".to_string(),
        };
        out.push_str(&format!("            {}: {},\n", field.name, default_val));
    }
    out.push_str("        }\n");
    out.push_str("    }\n");
    out.push_str("}\n\n");

    out.push_str(&format!(
        "pub const {}_SERVICE_NAME: &str = \"{}\";\n\n",
        upper_name, service_name
    ));

    out.push_str(&format!(
        "pub type {}Service<S> = publish_subscribe::PortFactory<S, {}, ()>;\n\n",
        msg.name, msg.name
    ));

    out.push_str(&format!(
        "pub fn open_or_create_{}_service<S: iceoryx2::service::Service>(\n",
        snake_name
    ));
    out.push_str("    node: &Node<S>,\n");
    out.push_str("    service_name: &str,\n");
    out.push_str(&format!(
        ") -> Result<{}Service<S>, Box<dyn core::error::Error>> {{\n",
        msg.name
    ));
    out.push_str("    let service_name: ServiceName = service_name.try_into()?;\n");
    out.push_str("    Ok(node\n");
    out.push_str("        .service_builder(&service_name)\n");
    out.push_str(&format!("        .publish_subscribe::<{}>()\n", msg.name));
    out.push_str("        .open_or_create()?)\n");
    out.push_str("}\n\n");

    out.push_str(&format!(
        "pub fn open_or_create_default_{}_service<S: iceoryx2::service::Service>(\n",
        snake_name
    ));
    out.push_str("    node: &Node<S>,\n");
    out.push_str(&format!(
        ") -> Result<{}Service<S>, Box<dyn core::error::Error>> {{\n",
        msg.name
    ));
    out.push_str(&format!(
        "    open_or_create_{}_service(node, {}_SERVICE_NAME)\n",
        snake_name, upper_name
    ));
    out.push_str("}\n");

    out
}

fn to_snake_identifier(name: &str) -> String {
    let mut out = String::new();
    let mut prev_is_lower_or_digit = false;

    for ch in name.chars() {
        if ch.is_ascii_alphanumeric() {
            if ch.is_ascii_uppercase() {
                if prev_is_lower_or_digit && !out.ends_with('_') {
                    out.push('_');
                }
                out.push(ch.to_ascii_lowercase());
                prev_is_lower_or_digit = false;
            } else {
                out.push(ch.to_ascii_lowercase());
                prev_is_lower_or_digit = ch.is_ascii_lowercase() || ch.is_ascii_digit();
            }
        } else if !out.ends_with('_') && !out.is_empty() {
            out.push('_');
            prev_is_lower_or_digit = false;
        }
    }

    if out.is_empty() {
        return "message".to_string();
    }

    match out.chars().next() {
        Some(ch) if ch.is_ascii_digit() => format!("msg_{out}"),
        _ => out,
    }
}

#[cfg(test)]
mod tests {
    use super::generate;
    use crate::ast::{Field, FieldType, Message, PrimitiveType};

    #[test]
    fn generates_iceoryx2_service_helpers() {
        let msg = Message {
            name: "Pose2D".to_string(),
            fields: vec![Field {
                name: "x".to_string(),
                ty: FieldType::Primitive(PrimitiveType::Float64),
            }],
            constants: vec![],
        };

        let generated = generate(&msg, "ros/Pose2D");

        assert!(generated.contains("#[derive(Debug, Clone, Copy, PartialEq, ZeroCopySend)]"));
        assert!(generated.contains("#[type_name(\"Pose2D\")]"));
        assert!(generated.contains("pub const POSE2_D_SERVICE_NAME: &str = \"ros/Pose2D\";"));
        assert!(generated.contains("pub type Pose2DService<S>"));
        assert!(generated.contains("open_or_create_pose2_d_service"));
        assert!(generated.contains("open_or_create_default_pose2_d_service"));
    }
}
