use super::ctypes_type;
use crate::ast::{FieldType, Message, PrimitiveType};

pub fn generate(msg: &Message, service_name: &str) -> String {
    let mut out = String::new();

    out.push_str("# Auto-generated by iox2gen. Do not edit.\n\n");
    out.push_str("import ctypes\n\n");
    out.push_str(&format!("IOX2_PAYLOAD_TYPE_NAME: str = \"{}\"\n", msg.name));
    out.push_str(&format!(
        "IOX2_SERVICE_NAME: str = \"{}\"\n\n",
        service_name
    ));

    for c in &msg.constants {
        out.push_str(&format!(
            "{} = {}\n",
            c.name,
            python_constant_literal(c.ty, &c.value)
        ));
    }
    if !msg.constants.is_empty() {
        out.push('\n');
    }

    // Emit a concrete _StaticString<N> class for each unique bounded-string capacity used.
    let mut seen_capacities: Vec<usize> = msg
        .fields
        .iter()
        .filter_map(|f| {
            if let FieldType::BoundedString(n) = f.ty {
                Some(n)
            } else {
                None
            }
        })
        .collect();
    seen_capacities.sort_unstable();
    seen_capacities.dedup();

    for n in &seen_capacities {
        // Memory layout mirrors Rust's StaticString<N>: data[N] + terminator + (padding) + len:u64
        out.push_str(&format!("class _StaticString{}(ctypes.Structure):\n", n));
        out.push_str(&format!(
            "    \"\"\"Fixed-capacity string (string<={n}). \
             Zero-copy compatible with iceoryx2_bb_container::string::StaticString<{n}>.\"\"\"\n"
        ));
        out.push_str("    _fields_ = [\n");
        out.push_str(&format!("        (\"data\", ctypes.c_char * {}),\n", n));
        out.push_str("        (\"terminator\", ctypes.c_char),\n");
        out.push_str("        (\"len\", ctypes.c_uint64),\n");
        out.push_str("    ]\n\n");
    }

    out.push_str(&format!("class {}(ctypes.Structure):\n", msg.name));
    out.push_str("    _fields_ = [\n");

    for f in &msg.fields {
        let field_type = match &f.ty {
            FieldType::Primitive(p) => ctypes_type(*p).to_string(),
            FieldType::FixedArray(p, n) => format!("{} * {}", ctypes_type(*p), n),
            FieldType::BoundedString(n) => format!("_StaticString{}", n),
        };
        out.push_str(&format!("        (\"{}\", {}),\n", f.name, field_type));
    }

    out.push_str("    ]\n");
    out
}

fn python_constant_literal(ty: PrimitiveType, raw: &str) -> String {
    if ty != PrimitiveType::Bool {
        return raw.to_string();
    }

    match raw.trim().to_ascii_lowercase().as_str() {
        "true" | "1" => "True".to_string(),
        "false" | "0" => "False".to_string(),
        _ => raw.to_string(),
    }
}

#[cfg(test)]
mod tests {
    use super::generate;
    use crate::ast::{Constant, Field, FieldType, Message, PrimitiveType};

    #[test]
    fn generates_ctypes_structure() {
        let msg = Message {
            name: "Pose".to_string(),
            fields: vec![
                Field {
                    name: "x".to_string(),
                    ty: FieldType::Primitive(PrimitiveType::Float64),
                },
                Field {
                    name: "flags".to_string(),
                    ty: FieldType::FixedArray(PrimitiveType::Uint8, 3),
                },
            ],
            constants: vec![],
        };

        let generated = generate(&msg, "ros/Pose");

        assert!(generated.contains("import ctypes"));
        assert!(generated.contains("class Pose(ctypes.Structure):"));
        assert!(generated.contains("(\"x\", ctypes.c_double)"));
        assert!(generated.contains("(\"flags\", ctypes.c_uint8 * 3)"));
    }

    #[test]
    fn generates_static_string_helper() {
        let msg = Message {
            name: "Sensor".to_string(),
            fields: vec![Field {
                name: "label".to_string(),
                ty: FieldType::BoundedString(64),
            }],
            constants: vec![],
        };

        let generated = generate(&msg, "ros/Sensor");

        assert!(generated.contains("class _StaticString64(ctypes.Structure):"));
        assert!(generated.contains("(\"data\", ctypes.c_char * 64)"));
        assert!(generated.contains("(\"terminator\", ctypes.c_char)"));
        assert!(generated.contains("(\"len\", ctypes.c_uint64)"));
        assert!(generated.contains("(\"label\", _StaticString64)"));
    }

    #[test]
    fn generates_python_service_constants() {
        let msg = Message {
            name: "Pose".to_string(),
            fields: vec![],
            constants: vec![],
        };

        let generated = generate(&msg, "ros/Pose");

        assert!(generated.contains("IOX2_PAYLOAD_TYPE_NAME: str = \"Pose\""));
        assert!(generated.contains("IOX2_SERVICE_NAME: str = \"ros/Pose\""));
    }

    #[test]
    fn converts_bool_constants_to_python_bool_literals() {
        let msg = Message {
            name: "Flags".to_string(),
            fields: vec![],
            constants: vec![Constant {
                name: "READY".to_string(),
                ty: PrimitiveType::Bool,
                value: "true".to_string(),
            }],
        };

        let generated = generate(&msg, "ros/Flags");
        assert!(generated.contains("READY = True"));
    }
}
